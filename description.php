                    <h1>Etat de la technique actuelle et présentation de l'invention</h1>
                    
                    <p>Pour naviguer à la voile il faut maintenir l’incidence correcte de la voilure par rapport à la direction du vent. Les interventions sur les réglages de la voilure qui nécessitent des manœuvres délicates et des efforts de tension très importants sont limitées autant que possible. Tant que les fluctuations de direction du vent restent modérées on préfère dévier le bateau plutôt que de modifier les réglages de la voilure. En conséquence il faut intervenir fréquemment sur le cap, et la trajectoire vers la destination est rallongée.</p>
                    <p>La poussée du vent sur les voiles incline les bateaux et augmente la traînée. Sur les monocoques cette inclinaison est limitée par du lest généralement suspendu à la quille. Cela alourdit le bateau et  limite l'évolution en eaux peu profondes. Sur les multicoques on utilise de grands  flotteurs  latéraux qui n'éliminent pas  le risque de chavirage. Les planches à voile inclinées au vent et les kitesurf  tirés par cerf-volant bénéficient - au contraire - d’un allègement réduisant la trainée, mais leur pilotage acrobatique les réserve à des sportifs entraînés.</p>
                    <p>Sur les gréements les plus couramment utilisés  la voilure est tendue fortement entre le haut du mât et la  bôme afin de maintenir un profil aérodynamique performant, ce qui implique des efforts de compression très importants sur le mât.</p>
                    <p>D'autres types de voiles  sont parfois disposés à l'avant, spinnaker notamment. Elles sont réservées aux  vents portants car il est difficile de leur donner un  profil  aérodynamique performant et leur empannage implique des manœuvres délicates.</p>
                    <p>Les dispositifs selon l'invention suppriment ou réduisent ces inconvénients :<br />On a adopté le concept pendulaire qui régit la stabilité aérodynamique des parapentes et deltaplanes dans le plan vertical, mais transposé dans le plan horizontal. Il n’est plus nécessaire de modifier le cap pour dévier la voilure, car elle pivote indépendamment du bateau et maintient sans intervention son incidence par rapport à la direction du vent.
    </p>
                    <p>La  fixation pendulaire du  dispositif  sur le bateau, sur laquelle s’exerce la force de traction, est  beaucoup plus basse que le centre de voilure sur les bateaux  traditionnels. En conséquence le couple de renversement est beaucoup plus faible et il n’est pas nécessaire de recourir à  du lest ou à de volumineux flotteurs pour limiter la gîte. L'inclinaison de la voilure vers l’arrière oriente la poussée du vent vers le haut, ce qui allège le voilier et  réduit sa traînée. Les forces nécessaires pour maintenir le profil aérodynamique de la voile sont supportées pour l'essentiel par les suspentes reliées à la patte d'amarrage.</p>
                    <p>L'ensemble voilure - gréement étant indépendant du bateau, il peut être déposé pour les manœuvres sans vent ou largué en cas de sinistre.</p>
                    <p>A l’arrêt et dans les vents faibles,  une structure reposant à l’avant sur de petits flotteurs pivotants peut supporter la voilure, mais lorsque le vent est suffisant  elle  peut être soulevée pour une navigation plus sportive.</p>
                    <p>Il faut noter que les dispositifs selon l'invention ne permettent pas de virer face au vent. Cet inconvénient, que l’on retrouve aussi sur les anciens voiliers, les kitesurf, les praos et  autres engins, est compensé  par la facilité avec laquelle les diverses manœuvres peuvent être réalisées. Pour voir une démonstration en images du déroulement d'un empannage, cliquez <a href="#empannage">ici</a>.</p>
